function Q = Bspline(a)

size_a = size(a);
P_N = size_a(2) - 3;
k = 0;
for i=1:P_N; 
    for u=0:0.01:1; 
        b0=1.0./6.*(1-u).^3; 
        b1=1.0./6.*(3.*u.^3-6.*u.^2+4); 
        b2=1.0./6.*(-3.*u.^3+3.*u.^2+3.*u+1); 
        b3=1.0./6.*u.^3; 
        k = k + 1;
        Q(1,k)=b0.*a(1,i)+b1.*a(1,i+1)+b2.*a(1,i+2)+b3.*a(1,i+3); 
        Q(2,k)=b0.*a(2,i)+b1.*a(2,i+1)+b2.*a(2,i+2)+b3.*a(2,i+3); 
    end
end